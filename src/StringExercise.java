import java.util.Scanner;

public class StringExercise {
    public static void main(String[] args) {

        String pinkFloyd = "We don't need no education\n" +
                           "We don't need no thought control";
        System.out.println(pinkFloyd);


        String p1 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(p1);


        String p2 = "In windows, the main drive is usually C:\\";
        System.out.println(p2);

        String p3 = "I can do backslashes \\, double backslashes \\\\,\n" +
                     "and the amazing triple backslash \\\\\\!";
        System.out.println(p3);
    }
}


